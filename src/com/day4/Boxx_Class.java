package com.day4;


class Boxx {
    double width;
    double height;
    double depth;

    public Boxx(double width, double height, double depth) {
        this.width = width;
        this.height = height;
        this.depth = depth;
    }

    public String toString() {
        return "Boxx [width=" + this.width + ", height=" + this.height + ", depth=" + this.depth + "]";
    }
}

